// 컴포넌트는 종류가 [함수형 , class형] 이 있다.
// class형 컴포넌트는 거의 사용하지 않지만, 알고는 넘어가자 (상식으로)
// >> 함수형 컴포넌트로 넘어가는 추세

// class 컴포넌트를 알고 가야하는 이유 (코드가 길고 복잡함)
// 1. 이미 전부터 class형으로 작업 해놓은 회사가 있을 수 있기 때문에
// 2. 그래서 코드 분석을 해야하고 뭔지는 알아야 하기 때문에
// 3. 자료를 찾아보면 class로 작성된 공식 문서가 있을 수 있기 때문에
// 4. 과거에는 lifecycle(리액트의 생명주기)이 class 컴포넌트에서만 동작했다.(현재는 함수형에서도 동작한다. 더 간단하고 편리함)
